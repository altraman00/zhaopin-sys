@Entity
@Table(name = "bi_report_resume_user", schema = "db_feo_zhaopin", catalog = "")
public class BiReportResumeUserEntity {
    private long id;
    private int uid;
    private String resumeId;
    private String unInviteReason;
    private Timestamp createTime;
    private Timestamp updateTime;

    @Id
    @Column(name = "id")
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Basic
    @Column(name = "uid")
    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    @Basic
    @Column(name = "resume_id")
    public String getResumeId() {
        return resumeId;
    }

    public void setResumeId(String resumeId) {
        this.resumeId = resumeId;
    }

    @Basic
    @Column(name = "un_invite_reason")
    public String getUnInviteReason() {
        return unInviteReason;
    }

    public void setUnInviteReason(String unInviteReason) {
        this.unInviteReason = unInviteReason;
    }

    @Basic
    @Column(name = "create_time")
    public Timestamp getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Timestamp createTime) {
        this.createTime = createTime;
    }

    @Basic
    @Column(name = "update_time")
    public Timestamp getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Timestamp updateTime) {
        this.updateTime = updateTime;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        BiReportResumeUserEntity that = (BiReportResumeUserEntity) o;
        return id == that.id &&
                uid == that.uid &&
                Objects.equals(resumeId, that.resumeId) &&
                Objects.equals(unInviteReason, that.unInviteReason) &&
                Objects.equals(createTime, that.createTime) &&
                Objects.equals(updateTime, that.updateTime);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, uid, resumeId, unInviteReason, createTime, updateTime);
    }
}
